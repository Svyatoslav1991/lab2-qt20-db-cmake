#ifndef MYRECT_H
#define MYRECT_H

#include <QColor>
#include <Qt>

/**
 * @brief Примитив прямоугольника для хранения данных в модели.
 *
 * Структура хранит геометрию прямоугольника (координаты левого верхнего угла и размеры)
 * и параметры пера для рисования (цвет, стиль, толщину).
 *
 * @note QObject не используется, т.к. метаинформация, сигналы/слоты и свойства не требуются.
 */
struct MyRect
{
    /// Цвет пера.
    QColor penColor { Qt::black };

    /// Стиль линии пера.
    Qt::PenStyle penStyle { Qt::SolidLine };

    /// Толщина пера (в пикселях). Рекомендуется >= 1.
    int penWidth { 1 };

    /// X координата левого верхнего угла.
    int left { 0 };

    /// Y координата левого верхнего угла.
    int top { 0 };

    /// Ширина прямоугольника (в пикселях). Рекомендуется >= 0.
    int width { 10 };

    /// Высота прямоугольника (в пикселях). Рекомендуется >= 0.
    int height { 10 };

    /**
     * @brief Конструктор по умолчанию.
     *
     * Создаёт прямоугольник со значениями по умолчанию:
     * чёрное перо, сплошная линия, толщина 1, геометрия (0,0,10,10).
     */
    MyRect() = default;

    /**
     * @brief Конструктор для инициализации всех полей.
     * @param color Цвет пера.
     * @param style Стиль линии пера.
     * @param pWidth Толщина пера (в пикселях).
     * @param l X координата левого верхнего угла.
     * @param t Y координата левого верхнего угла.
     * @param w Ширина прямоугольника.
     * @param h Высота прямоугольника.
     */
    MyRect(const QColor& color,
           Qt::PenStyle style,
           int pWidth,
           int l,
           int t,
           int w,
           int h)
        : penColor(color)
        , penStyle(style)
        , penWidth(pWidth)
        , left(l)
        , top(t)
        , width(w)
        , height(h)
    {}
};

#endif // MYRECT_H
